    tiles(i,j,10)=1;
end
if(x==17)
    tiles(i,j,8)=1;
end
if(x==18)
    tiles(i,j,11)=1;
    tiles(i,j,7)=1;
end
if(x==19)
    tiles(i,j,11)=1;
    tiles(i,j,10)=1;
end
