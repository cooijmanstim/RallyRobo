    tiles(i,j,4)=1;
end
if(x==9)
    tiles(i,j,6)=1;
end
if(x==10)
    tiles(i,j,3)=1;
    tiles(i,j,4)=1;
end
if(x==11)
    tiles(i,j,3)=1;
    tiles(i,j,6)=1;
end
if(x==12)
    tiles(i,j,4)=1;
    tiles(i,j,5)=1;
end
if(x==13)
    tiles(i,j,5)=1;
    tiles(i,j,6)=1;
end
